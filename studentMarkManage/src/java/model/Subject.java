package model;

/**
 *
 * @author quang
 */
public class Subject {
    private String id;
    private Teacher teacher;
    private String name;

    public Subject() {
    }

    public Subject(String id, Teacher teacher, String name) {
        this.id = id;
        this.teacher = teacher;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Teacher getTeacher() {
        return teacher;
    }

    public void setTeacher(Teacher teacher) {
        this.teacher = teacher;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Subject{" + "id=" + id + ", teacher=" + teacher + ", name=" + name + '}';
    }
    
}
